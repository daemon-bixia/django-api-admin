# Tagging Endpoints


def find_endpoint_urls(path, endpoints, url_prefix, result):
    """
    Finds the endpoint_urls that match the path.pattern in the endpoints generated by site_generator or admin_generator,
    it also checks if that this endpoint exists in result["paths"] of the OpenAPI schema generated by the schema view.
    """
    endpoint_urls = []
    for endpoint in endpoints:
        # Extract the endpoint's route representation used in the OpenAPI scheme
        endpoint_url = url_prefix + endpoint[0]
        # Check that the endpoint exists in the ModelAdmin's, or AdminSite's OpenAPI endpoints
        if result["paths"].get(endpoint_url, None):
            # Extract the endpoint's route used in the urlpatterns list
            endpoint_url_pattern = (
                endpoint[1][1:] if endpoint[1].startswith("/") else endpoint[1]
            )
            if isinstance(path, URLResolver):
                for inner_path in path.url_patterns:
                    full_pattern = str(path.pattern) + str(inner_path.pattern)
                    if endpoint_url_pattern == full_pattern:
                        endpoint_urls.append(endpoint_url)
            elif endpoint_url_pattern == str(path.pattern):
                endpoint_urls.append(endpoint_url)
    return endpoint_urls


def tag_result_paths(urlpatterns, endpoints, site, result, tag_name):
    """
    Tag method of the endpoints in result[paths] with the tag_name.
    """
    for path in urlpatterns:
        endpoint_urls = find_endpoint_urls(
            path, endpoints, site.url_prefix, result)
        for endpoint_url in endpoint_urls:
            # Tag all the methods of the endpoint with the tag_name
            for method, body in result["paths"][endpoint_url].items():
                result["paths"][endpoint_url][method] = {
                    **body, "tags": [tag_name]}
    return result


# Site Views Dynamic Schema


def add_site_views_dynamic_schema(result, site, request):
    """
    Dynamically add schemas and update paths for the AdminSite.
    """
    # Add permission view response schema
    permission_classes = site.get_permission_classes(request)
    schema_name = f"{site.name.title()}SitePermissions"

    # Ensure components and schemas exist in the result dictionary
    result.setdefault("components", {}).setdefault("schemas", {})

    # Helper to build description/help_text for a permission class
    def get_permission_description(perm_cls):
        if perm_cls.__doc__:
            doc = " ".join(
                [line.strip()
                 for line in perm_cls.__doc__.splitlines() if line.strip()]
            )
            if doc:
                return doc
        message = getattr(perm_cls, "message", None)
        if message:
            return str(message)
        return (
            f"Indicates whether the user satisfies the {perm_cls.__name__} permission."
        )

    # Build the schema for permissions
    result["components"]["schemas"][schema_name] = {
        "type": "object",
        "properties": {
            perm_cls.__name__: {
                "type": "boolean",
                "readOnly": True,
                "description": get_permission_description(perm_cls),
            }
            for perm_cls in permission_classes
        },
        "required": [perm_cls.__name__ for perm_cls in permission_classes],
        "description": "The permission check results mapped by permission class names.",
    }

    # Reference this component in the path's response section
    permissions_path = f"{site.url_prefix}/permissions/"
    if permissions_path in result.get("paths", {}):
        path_item = result["paths"][permissions_path]
        if "get" in path_item:
            # Update the 200 response to point to the new permissions schema
            response_200 = (
                path_item["get"].setdefault(
                    "responses", {}).setdefault("200", {})
            )
            content = response_200.setdefault("content", {})
            json_content = content.setdefault("application/json", {})
            json_content["schema"] = {
                "type": "object",
                "properties": {
                    "status": {
                        "type": "integer",
                        "description": "The response status code.",
                    },
                    "data": {"$ref": f"#/components/schemas/{schema_name}"},
                },
                "required": ["status", "data"],
            }

            # Add the success response example
            json_content["examples"] = {
                "Success Response": {
                    "value": {
                        "status": 200,
                        "data": {
                            perm_cls.__name__: True for perm_cls in permission_classes
                        },
                    }
                }
            }

    # Add operation id to openAPI view
    schema_path = f"{site.url_prefix}/openapi-specification-schema/"
    if schema_path in result.get("paths", {}):
        path_item = result["paths"][schema_path]
        if "get" in path_item:
            get = path_item["get"]
            get["operationId"] = "Retrieve openapi schema"

    return result


def build_serializer_schema(result, inspector, serializer_class, json_content, direction="response"):
    """
    Resolve a serializer into an OpenAPI component
    """
    resolved = inspector.resolve_serializer(
        serializer_class, direction=direction)
    # Include the component in `schema/components` if not already included
    if resolved.name not in result["components"]["schemas"]:
        result.setdefault("components", {}).setdefault("schemas", {})[
            resolved.name] = resolved.schema
    return resolved.ref


def build_serializer_with_inlines_schema(result, inspector, model_admin, request, json_content, add=False):
    """
    Add model's serializer schema along with inlines schema representation.

    Resolves the model admin's serializer and each of its inline serializers,
    registers the resulting components into ``result["components"]["schemas"]``,
    and populates the ``data`` and ``inlines`` properties on ``json_content["schema"]``.
    """
    # Add the serializer auto schema reference as `data`
    serializer_class = model_admin.get_serializer_class(request)
    resolved_ref = build_serializer_schema(
        result, inspector, serializer_class, json_content)
    json_content["schema"]["properties"]["data"]["allOf"] = [resolved_ref]
    # Add a list of auto schema references for every inline model
    inlines = model_admin.inlines
    for inline in inlines:
        inline_instance = inline(model_admin.model, model_admin.admin_site)
        serializer_class = inline_instance.get_serializer_class(request)
        resolved = inspector.resolve_serializer(
            serializer_class,
            direction="response"
        )
        # Include the component in `schema/components` if not already included
        if resolved.name not in result["components"]["schemas"]:
            result.setdefault("components", {}).setdefault("schemas", {})[
                resolved.name] = resolved.schema

        # Add a schema for inline model representing every inline operation.
        inline_app_label = inline.model._meta.app_label
        inline_model_name = inline.model._meta.verbose_name
        model_id = f"{inline_app_label}.{inline_model_name}"
        json_content["schema"]["properties"]["inlines"]["properties"][model_id] = {
            "type": "object",
            "description": f"Operations for inline model {inline_app_label}.{inline_model_name}",
            "properties": {
                "add": {
                    "type": "array",
                    "description": "List of added inline objects",
                    "items": {
                        "$ref": f"#/components/schemas/{resolved.name}"
                    }
                },
                "change": {
                    "type": "array",
                    "description": "List of changed inline objects",
                    "items": {
                        "$ref": f"#/components/schemas/{resolved.name}"
                    }
                },
                "delete": {
                    "type": "array",
                    "description": "List of deleted inline objects",
                    "items": {
                        "$ref": f"#/components/schemas/{resolved.name}"
                    }
                }
            }
        }
        if add:
            del json_content["schema"]["properties"]["inlines"]["properties"][model_id]["properties"]["change"]
            del json_content["schema"]["properties"]["inlines"]["properties"][model_id]["properties"]["delete"]


def build_changelist_action_request_schema(result, inspector, model_admin, request, json_content):
    """
    Build the request schema for changelist actions.
    """
    try:
        model_admin.get_changelist_instance(request)
    except Exception:
        pass

    serializer_class = model_admin.get_action_serializer_class(request)

    return build_serializer_schema(result, inspector, serializer_class, json_content, direction="request")


def add_model_admin_views_dynamic_schema(result, site, model_urls, model, request, generator):
    app_label, model_name = model._meta.app_label, model._meta.verbose_name
    model_admin = site.get_model_admin(model)

    inspector = AutoSchema()
    inspector.registry = generator.registry

    _view = APIView()
    if request and hasattr(request, "parser_context"):
        _view.request = request
    else:
        django_request = RequestFactory().get("/fake-path/")
        _view.request = Request(django_request)
    inspector.view = _view

    for url in model_urls:
        if url.name == f"{app_label}_{model_name}_change":
            change_path = result.setdefault("paths", {}).setdefault(
                f"{site.url_prefix}/{app_label}/{model_name}/{{object_id}}/change/", {})
            if change_path:
                # Add `operationId` to ChangeView
                change_path.setdefault("get", {})[
                    "operationId"] = f"Get change form for {app_label}.{model_name}"
                patch = change_path.setdefault("patch", {})
                patch["operationId"] = f"Update {app_label}.{model_name}"
                # Add dynamic response schema to ChangeView
                response_200 = patch.setdefault(
                    "responses", {}).setdefault("200", {})
                content = response_200.setdefault("content", {})
                json_content = content.setdefault("application/json", {})
                json_content["schema"] = {
                    "type": "object",
                    "properties": {
                        "detail": {
                            "type": "string",
                            "description": "A detail message about the bulk update operation",
                        },
                        "data": {
                            "description": "The updated model instance",
                        },
                        "inlines": {
                            "type": "object",
                            "properties": {},
                            "description": "The modified inlines instances",
                        }
                    },
                    "required": ["detail", "data", "inlines"]
                }
                # Populate `data` and `inlines` schema properties
                build_serializer_with_inlines_schema(
                    result, inspector, model_admin, request, json_content
                )
        elif url.name == f"{app_label}_{model_name}_delete":
            delete_path = result.setdefault("paths", {}).setdefault(
                f"{site.url_prefix}/{app_label}/{model_name}/{{object_id}}/delete/", {})
            if delete_path:
                # Add `operationId` to delete view
                delete = delete_path.setdefault("delete", {})
                delete["operationId"] = f"Delete {app_label}.{model_name}"
        elif url.name == f"{app_label}_{model_name}_detail":
            detail_path = result.setdefault("paths", {}).setdefault(
                f"{site.url_prefix}/{app_label}/{model_name}/{{object_id}}/detail/", {})
            if detail_path:
                # Add `operationId` to detail view
                get = detail_path.setdefault("get", {})
                get["operationId"] = f"Retrieve {app_label}.{model_name}"
                # Add dynamic response schema to ChangeView
                response_200 = get.setdefault(
                    "responses", {}).setdefault("200", {})
                content = response_200.setdefault("content", {})
                json_content = content.setdefault("application/json", {})
                serializer_class = model_admin.get_serializer_class(request)
                resolved_ref = build_serializer_schema(
                    result, inspector, serializer_class, json_content
                )
                json_content["schema"] = resolved_ref
        elif url.name == f"{app_label}_{model_name}_add":
            add_path = result.setdefault("paths", {}).setdefault(
                f"{site.url_prefix}/{app_label}/{model_name}/add/", {})
            if add_path:
                # Add `operationId` to AddView
                get = add_path.setdefault("get", {})
                get["operationId"] = f"Get add form for {app_label}.{model_name}"
                post = add_path.setdefault("post", {})
                post["operationId"] = f"Create {app_label}.{model_name}"
                # Add dynamic response schema to add view
                response_200 = post.setdefault(
                    "responses", {}).setdefault("200", {})
                content = response_200.setdefault("content", {})
                json_content = content.setdefault("application/json", {})
                json_content["schema"] = {
                    "type": "object",
                    "properties": {
                        "detail": {
                            "type": "string",
                            "description": "A detail message about the bulk update operation",
                        },
                        "data": {
                            "description": "The updated model instance",
                        },
                        "inlines": {
                            "type": "object",
                            "properties": {},
                            "description": "The modified inlines instances",
                        }
                    },
                    "required": ["detail", "data", "inlines"]
                }
                # Populate `data` and `inlines` schema properties
                build_serializer_with_inlines_schema(
                    result, inspector, model_admin, request, json_content, add=True
                )
        elif url.name == f"{app_label}_{model_name}_changelist":
            changelist_path = result.setdefault("paths", {}).setdefault(
                f"{site.url_prefix}/{app_label}/{model_name}/changelist/", {})
            if changelist_path:
                # Add `operationId` to ChangeListView
                get = changelist_path.setdefault("get", {})
                get["operationId"] = f"Get {app_label}.{model_name} changelist"
                post = changelist_path.setdefault("post", {})
                post["operationId"] = f"Perform action on {app_label}.{model_name}"
                # Add dynamic request schema to ChangelistView.post
                request_body = post.setdefault("requestBody", {})
                content = request_body.setdefault("content", {})
                json_content = content.setdefault("application/json", {})
                resolved_ref = build_changelist_action_request_schema(
                    result, inspector, model_admin, request, json_content)
                json_content["schema"] = resolved_ref

    return result


# Modify Schema Hook


def modify_schema(result, generator, request, public):
    """
    A drf-spectacular hook that produces the package's OpenAPI schema.
    It adds the package's basic information (i.e like title, and description, etc),
    It also tags every AdminSite route by the site's name, and every ModelAdmin
    route by model_admin's model._meta.verbose_name.
    """
    # Change the api info
    result["info"] = {
        "title": _("Django API Admin"),
        "description": _(
            "A rewrite of django.contrib.admin as a Restful API, intended for use\t"
            "in the process of creating custom admin panels using frontend frameworks like"
            "react, and vue while maintaining an API similar to django.contrib.admin."
        ),
        "contact": "msbizzacc0unt@gmail.com",
        "license": {
            "name": "MIT License",
            "url": "https://github.com/demon-bixia/django-api-admin/blob/production/LICENSE",
        },
        "version": "1.0.0",
    }

    # Edit the tags for each path based on the model_admin or admin_site
    for site in all_sites:
        # Create a generator and parse the site urls so that we can compare them below
        site_generator = spectacular_settings.DEFAULT_GENERATOR_CLASS(
            patterns=site.site_urls
        )
        site_generator.parse(request, True)
        result = tag_result_paths(
            site.site_urls, site_generator.endpoints, site, result, site.name
        )
        result = add_site_views_dynamic_schema(result, site, request)

        # Update the urls for each registered model
        for model in site._registry.keys():
            model_urls = site.admin_urls.get(model, None)
            if model_urls:
                # Create a generator and parse the admin urls so that we can compare them below
                admin_generator = spectacular_settings.DEFAULT_GENERATOR_CLASS(
                    patterns=model_urls
                )
                admin_generator.parse(request, True)
                result = tag_result_paths(
                    model_urls,
                    admin_generator.endpoints,
                    site,
                    result,
                    model._meta.verbose_name,
                )
                result = add_model_admin_views_dynamic_schema(
                    result,
                    site,
                    model_urls,
                    model,
                    request,
                    generator
                )

    return result
